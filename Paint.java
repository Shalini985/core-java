class Paint{
 static int paintId;
 static String name;
 static String color;
 
  public static void setPaintId(int id){
	  
	  paintId=id;
  }

 public static int getPaintId(){
	
	return paintId;
}
public static void setName(String nm){
	  
	  name=nm;
  }

 public static String getName(){
	
	return name;
}
public static void setColor(String co){
	  
	  color=co;
  }

 public static String getColor(){
	
	return color;
}






}